#include <math.h>

int add(int a,int b){
    return a+b;
}

float add(float a,float b){
    return a+b;
}

double add(double a,double b){
    return a+b;
}


// minus
int minus(int a,int b){
    return a-b;
}

float minus(float a,float b){
    return a-b;
}

double minus(double a,double b){
    return a-b;
}


